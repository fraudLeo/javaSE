public class User {
    public User() {
        System.out.println("User's Constructor is invoked");
    }
    public User(int i) {
        System.out.println(i);
    }
    public User(String a) {
        System.out.println(a);
    }
    public User(int z, String x) {
        System.out.println(z+x);
    }
}
