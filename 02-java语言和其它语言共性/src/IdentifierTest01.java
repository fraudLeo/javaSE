/*
* 关于Java语言当中的标识符
* 1.什么是标识符?
    -在java源程序当中,凡是程序员可以自己定义,命名的单词都是标识符.
    - 标识符可以标识的元素:
        * 类名
        * 方法名
        * 变量名
        * 接口名
        * 常量名
        ......

* 2.标识符的命名规则?
    * 一个合法的标识符只能由数字,字母,下划线,美元符号组成
    * 不能以数字开始
    * 严格区分大小写
    * 关键字不能做标识符
    * 理论上无长度限制

* 标识符的命名规范?
    * 最好见名知意
    * 遵守驼峰命名方式
    * 类名`接口名:首字母大写,后面每个单词收字母大写
    * 变量名`方法名:首字母小写,后面每个单词首字母大写.
    * 常量名: 全部大写.
*/
