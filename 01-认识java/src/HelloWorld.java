//public 表示是公开的
//class表示定义一个类
//HelloWorld 表示一个类名
public class HelloWorld {//表示定义一个公开的类,起名为HelloWorld

    /**
     * public 表示公开的
     * static 表示静态的
     * void 表示返回值是空
     * main表示方法名是main
     * (String[] args) 是一个main方法的形式参数列表
     */

    public static void main(String[] args) {//表示定义一个公开的 静态的 之方法------程序的入口
        //方法体开始
        System.out.println("Hello World!");
    }
}
